int		ft_atoi(char *str, int tam)
{
	int num;
	int i;

	num = 0;
	i = 0;
	while ((str[i] >= '0' && str[i] <= '9') && (i < tam - 4))
	{
		num = (num * 10) + (str[i] - '0');
		i++;
	}
	return (num);
}
