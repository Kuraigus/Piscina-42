void	ft_save_char(char *str, int tam, char *save)
{
	int i;
	int j;

	j = 0;
	i = tam - 4;
	while (str[i] != '\n')
	{
		save[j] = str[i];
		j++;
		i++;
	}
	save[j] = '\0';
}
